package Modulo2.RepasoParcial.util;

public class Ciudad2 {
    private String nombre;
    private int fase;
    
    public Ciudad2(String nombre, int fase) {
        this.nombre = nombre;
        this.fase = fase;
    }

    public String getNombre() {
        return this.nombre;
    }

    public int getFase() {
        return this.fase;
    }
}
